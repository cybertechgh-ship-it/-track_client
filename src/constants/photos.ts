// Stable photo assignment: uses a hash of the driver/vehicle name to always pick the same photo
export function getStablePhoto(id: number, name: string, photos: string[]): string {
  let hash = 0;
  for (let i = 0; i < name.length; i++) {
    hash = ((hash << 5) - hash + name.charCodeAt(i)) | 0;
  }
  const index = Math.abs(hash + id) % photos.length;
  return photos[index];
}
